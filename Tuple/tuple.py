# Tuple access using index
t = (10, 20, 30, 40, 50)

print("First element:", t[0])
print("Last element:", t[-1])
print("Third element:", t[2])
print("Third last element:", t[-3])

t = (10, 50, 30)
print(max(t))
print(min(t))
print(sum(t))
print(len(t))
print(sorted(t))
print(tuple(reversed(t)))
print(t.count(10))
print(t.index(30))
print("Accessing Tuple elements using indexing:")